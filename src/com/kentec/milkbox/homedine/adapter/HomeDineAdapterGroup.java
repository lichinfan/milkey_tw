package com.kentec.milkbox.homedine.adapter;

public class HomeDineAdapterGroup {
	private StoreAdapter storeAdapter;
	private FoodAdapter foodAdapter;
	private MealListAdapter mealListAdapter;

	public StoreAdapter getStoreAdapter() {
		return storeAdapter;
	}

	public void setStoreAdapter(StoreAdapter storeAdapter) {
		this.storeAdapter = storeAdapter;
	}

	public FoodAdapter getFoodAdapter() {
		return foodAdapter;
	}

	public void setFoodAdapter(FoodAdapter foodAdapter) {
		this.foodAdapter = foodAdapter;
	}

	public MealListAdapter getMealListAdapter() {
		return mealListAdapter;
	}

	public void setMealListAdapter(MealListAdapter mealListAdapter) {
		this.mealListAdapter = mealListAdapter;
	}

}
