package com.kentec.milkbox.homedine.data;

public class StoreData {

	private String storeID;
	private String storeCategoryId;
	private String storeName;
	private String storeAbout;
	private String storeThumbnail;
	private String storePic;

	public String getStoreID() {
		return storeID;
	}

	public void setStoreID(String storeID) {
		this.storeID = storeID;
	}

	public String getStoreCategoryId() {
		return storeCategoryId;
	}

	public void setStoreCategoryId(String storeCategoryId) {
		this.storeCategoryId = storeCategoryId;
	}

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getStoreAbout() {
		return storeAbout;
	}

	public void setStoreAbout(String storeAbout) {
		this.storeAbout = storeAbout;
	}

	public String getStorePic() {
		return storePic;
	}

	public void setStorePic(String storePic) {
		this.storePic = storePic;
	}

	public String getStoreThumbnail() {
		return storeThumbnail;
	}

	public void setStoreThumbnail(String storeThumbnail) {
		this.storeThumbnail = storeThumbnail;
	}

}
