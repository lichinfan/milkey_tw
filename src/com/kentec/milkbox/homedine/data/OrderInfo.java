package com.kentec.milkbox.homedine.data;

import java.util.ArrayList;

public class OrderInfo {

	private String orderId;
	private String customerFirstname;
	private String customerLastname;
	private String billingAddres;
	private String phone;
	private ArrayList<OrderInfoStatusHistory> statusHistoryList;
	private ArrayList<OrderInfoItems> itemList;

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getCustomerFirstname() {
		return customerFirstname;
	}

	public void setCustomerFirstname(String customerFirstname) {
		this.customerFirstname = customerFirstname;
	}

	public String getCustomerLastname() {
		return customerLastname;
	}

	public void setCustomerLastname(String customerLastname) {
		this.customerLastname = customerLastname;
	}

	public String getBillingAddres() {
		return billingAddres;
	}

	public void setBillingAddres(String billingAddres) {
		this.billingAddres = billingAddres;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public ArrayList<OrderInfoStatusHistory> getStatusHistoryList() {
		return statusHistoryList;
	}

	public void setStatusHistoryList(ArrayList<OrderInfoStatusHistory> statusHistoryList) {
		this.statusHistoryList = statusHistoryList;
	}

	public ArrayList<OrderInfoItems> getItemList() {
		return itemList;
	}

	public void setItemList(ArrayList<OrderInfoItems> itemList) {
		this.itemList = itemList;
	}
}
