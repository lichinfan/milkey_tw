package com.kentec.milkbox.asr;

public abstract class ASRCMDAdapter
{

	private String[] command;

	public ASRCMDAdapter(String[] command)
	{
		this.command = command;
	}

	public String[] getCommand()
	{
		return this.command;
	}

	public abstract void action();
	
	public abstract void action(String strValue);

}
